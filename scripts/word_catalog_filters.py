def is_playable_word(word: str) -> bool:
    if word.endswith(("ed", "ing", "ly")):
        return False

    return (
        bool(WORD_PATTERN.fullmatch(word))
        and any(letter in word for letter in "aeiou")
        and word not in STOPWORDS
        and word not in BLOCKLIST
    )
