create or replace function public.generate_room_code()
returns text
language plpgsql
as $$
declare
  chars text := 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  code text := '';
  i integer;
begin
  for i in 1..6 loop
    code := code || substr(chars, 1 + floor(random() * length(chars))::int, 1);
  end loop;

  return code;
end;
$$;

create or replace function public.is_room_member(target_room_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.room_players member_row
    where member_row.room_id = target_room_id
      and member_row.user_id = auth.uid()
  );
$$;

create or replace function public.create_room(p_display_name text default 'Player')
returns table (room_id uuid, room_code text, seat_index integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  clean_name text;
  generated_code text;
  generated_room_id uuid;
  tries integer := 0;
begin
  if auth.uid() is null then
    raise exception 'Authentication required.';
  end if;

  clean_name := left(coalesce(nullif(trim(p_display_name), ''), 'Player'), 32);

  loop
    tries := tries + 1;
    generated_code := public.generate_room_code();

    begin
      insert into public.rooms (code, host_user_id, status, max_players)
      values (generated_code, auth.uid(), 'waiting', 4)
      returning id into generated_room_id;
      exit;
    exception when unique_violation then
      if tries >= 25 then
        raise exception 'Could not generate a unique room code.';
      end if;
    end;
  end loop;

  insert into public.room_players (room_id, user_id, display_name, seat_index, is_ready)
  values (generated_room_id, auth.uid(), clean_name, 0, false);

  insert into public.room_states (room_id, version, state_json, updated_by)
  values (generated_room_id, 1, '{}'::jsonb, auth.uid());

  return query
  select generated_room_id, generated_code, 0;
end;
$$;

create or replace function public.join_room(
  p_room_code text,
  p_display_name text default 'Player'
)
returns table (
  room_id uuid,
  room_code text,
  seat_index integer,
  host_user_id uuid,
  status text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  clean_name text;
  target_room public.rooms%rowtype;
  existing_row public.room_players%rowtype;
  chosen_seat integer;
begin
  if auth.uid() is null then
    raise exception 'Authentication required.';
  end if;

  clean_name := left(coalesce(nullif(trim(p_display_name), ''), 'Player'), 32);

  select *
  into target_room
  from public.rooms
  where code = upper(trim(p_room_code))
  for update;

  if target_room.id is null then
    raise exception 'Room not found.';
  end if;

  select *
  into existing_row
  from public.room_players
  where room_id = target_room.id
    and user_id = auth.uid();

  if existing_row.user_id is not null then
    update public.room_players
    set display_name = clean_name
    where room_id = existing_row.room_id
      and user_id = existing_row.user_id;

    return query
    select
      target_room.id,
      target_room.code,
      existing_row.seat_index,
      target_room.host_user_id,
      target_room.status;
    return;
  end if;

  if target_room.status <> 'waiting' then
    raise exception 'Room is not accepting new players.';
  end if;

  if (
    select count(*)
    from public.room_players player_row
    where player_row.room_id = target_room.id
  ) >= target_room.max_players then
    raise exception 'Room is full.';
  end if;

  select seat_num
  into chosen_seat
  from generate_series(0, target_room.max_players - 1) as seat_num
  where not exists (
    select 1
    from public.room_players player_row
    where player_row.room_id = target_room.id
      and player_row.seat_index = seat_num
  )
  order by seat_num
  limit 1;

  if chosen_seat is null then
    raise exception 'No open seat available.';
  end if;

  insert into public.room_players (room_id, user_id, display_name, seat_index, is_ready)
  values (target_room.id, auth.uid(), clean_name, chosen_seat, false);

  return query
  select
    target_room.id,
    target_room.code,
    chosen_seat,
    target_room.host_user_id,
    target_room.status;
end;
$$;

create or replace function public.leave_room(p_room_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  target_room public.rooms%rowtype;
  remaining_count integer;
  next_host uuid;
begin
  if auth.uid() is null then
    raise exception 'Authentication required.';
  end if;

  select *
  into target_room
  from public.rooms
  where id = p_room_id
  for update;

  if target_room.id is null then
    return;
  end if;

  delete from public.room_players
  where room_id = p_room_id
    and user_id = auth.uid();

  select count(*) into remaining_count
  from public.room_players
  where room_id = p_room_id;

  if remaining_count = 0 then
    delete from public.rooms where id = p_room_id;
    return;
  end if;

  if target_room.host_user_id = auth.uid() then
    select user_id
    into next_host
    from public.room_players
    where room_id = p_room_id
    order by seat_index
    limit 1;

    update public.rooms
    set host_user_id = next_host
    where id = p_room_id;
  end if;
end;
$$;

