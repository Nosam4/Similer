create extension if not exists pgcrypto;

create table if not exists public.rooms (
  id uuid primary key default gen_random_uuid(),
  code text not null unique check (code ~ '^[A-Z0-9]{6}$'),
  host_user_id uuid not null references auth.users (id) on delete cascade,
  status text not null default 'waiting' check (status in ('waiting', 'playing', 'finished')),
  max_players integer not null default 4 check (max_players between 2 and 8),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.room_players (
  room_id uuid not null references public.rooms (id) on delete cascade,
  user_id uuid not null references auth.users (id) on delete cascade,
  display_name text not null check (length(trim(display_name)) between 1 and 32),
  seat_index integer not null check (seat_index >= 0),
  is_ready boolean not null default false,
  joined_at timestamptz not null default now(),
  primary key (room_id, user_id),
  unique (room_id, seat_index)
);

create table if not exists public.room_states (
  room_id uuid primary key references public.rooms (id) on delete cascade,
  version integer not null default 1 check (version > 0),
  state_json jsonb not null default '{}'::jsonb,
  updated_by uuid references auth.users (id) on delete set null,
  updated_at timestamptz not null default now()
);

create table if not exists public.room_actions (
  id bigint generated by default as identity primary key,
  room_id uuid not null references public.rooms (id) on delete cascade,
  actor_user_id uuid not null references auth.users (id) on delete cascade,
  action_type text not null check (length(trim(action_type)) > 0),
  payload jsonb not null default '{}'::jsonb,
  accepted boolean not null default false,
  error_text text,
  version_before integer,
  version_after integer,
  created_at timestamptz not null default now()
);

create index if not exists room_players_room_id_idx on public.room_players (room_id);
create index if not exists room_actions_room_id_idx on public.room_actions (room_id);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

